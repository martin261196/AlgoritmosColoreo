package clasesUtiles;

public class Arista {
	private int nodoOrigen;
	private int nodoDestino;
	
	public Arista(int origen, int destino) {
		this.nodoOrigen = origen;
		this.nodoDestino = destino;
	}
	
	public int getNodoOrigen() {
		return nodoOrigen;
	}
	
	public int getNodoDestino() {
		return nodoDestino;
	}
	
	
}
